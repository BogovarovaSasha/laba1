#include <iostream>
int charToNum (char x) {
    return x - '0';
}
int main() {
std::cout << charToNum('0') << std::endl;
std::cout << charToNum('1') << std::endl;
std::cout << charToNum('2') << std::endl;
std::cout << charToNum('3') << std::endl;
std::cout << charToNum('4') << std::endl;
std::cout << charToNum('5') << std::endl;
std::cout << charToNum('6') << std::endl;
std::cout << charToNum('7') << std::endl;
std::cout << charToNum('8') << std::endl;
std::cout << charToNum('9') << std::endl;
return 0;
}